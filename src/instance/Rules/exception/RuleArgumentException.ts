import BaseException from '../../../exception/BaseException'

/**
 * Exception thrown when rule arguments are invalid or missing
 * 
 * This exception is used throughout the Rules system to indicate
 * when required parameters are missing or invalid for rule construction,
 * condition evaluation, or callback execution.
 */
export default class RuleArgumentException extends BaseException {
  /**
   * Creates a new RuleArgumentException instance
   * 
   * @param message - The error message describing the issue
   * @param fileName - Optional file name where the error occurred
   * @param lineNumber - Optional line number where the error occurred
   */
  constructor(
    message: string = 'An incorrect argument!',
    fileName: string | null = null,
    lineNumber: number | null = null
  ) {
    super(message, fileName, lineNumber)
    this.name = 'RuleArgumentException'
  }
}
