import Query from './Query'
import { gql } from '@urql/core'
import type { GraphQLResponse } from '../types/graphql'
import ResponsePolicy from '../response/ResponsePolicy'

/**
 * Variables for policy query
 */
export interface QueryPolicyVariables {
  metaType?: string;
  metaId?: string;
}

/**
 * Query for retrieving policy information
 */
export default class QueryPolicy extends Query {
  /**
   * @param graphQLClient - The URQL client wrapper
   * @param knishIOClient - The main KnishIO client instance
   */
  constructor (graphQLClient: any, knishIOClient: any) {
    super(graphQLClient, knishIOClient)

    this.$__query = gql`query( $metaType: String, $metaId: String, ) {
      Policy( metaType: $metaType, metaId: $metaId ) {
        molecularHash,
        position,
        metaType,
        metaId,
        conditions,
        callback,
        rule,
        createdAt
      }
    }`
  }

  /**
   * Creates a response instance for this query
   * @param json - The GraphQL response data
   * @returns Response instance
   */
  createResponse (json: GraphQLResponse): any {
    return new ResponsePolicy({
      query: this,
      json
    })
  }
}