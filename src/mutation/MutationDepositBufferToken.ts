import MutationProposeMolecule from './MutationProposeMolecule';

/**
 * Variables for depositing buffer tokens
 */
export interface MutationDepositBufferTokenVariables {
  amount: number | string;
  tradeRates: unknown;
}

/**
 * Mutation for depositing tokens into buffer
 */
export default class MutationDepositBufferToken extends MutationProposeMolecule {
  /**
   * Fills the Molecule with provided wallet and amount data
   *
   * @param amount - The amount to deposit
   * @param tradeRates - The trade rates configuration
   */
  fillMolecule ({
    amount,
    tradeRates
  }: MutationDepositBufferTokenVariables): void {
    this.$__molecule.initDepositBuffer?.({
      amount,
      tradeRates
    })
    this.$__molecule.sign?.({})
    this.$__molecule.check?.(this.$__molecule.sourceWallet)
  }
}