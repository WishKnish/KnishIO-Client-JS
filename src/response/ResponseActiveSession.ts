import Response from './Response'
import type { ResponseOptions } from '../types/response/interfaces'

/**
 * Response handler for ActiveSession queries
 * 
 * This class processes responses from queries that retrieve active session information,
 * providing structured access to session data from the Knish.IO network.
 */
export default class ResponseActiveSession extends Response {
  /**
   * Creates a new ResponseActiveSession instance
   * 
   * @param options - Response configuration options
   * @param options.query - The originating query instance
   * @param options.json - The raw JSON response from the GraphQL endpoint
   */
  constructor({ query, json }: ResponseOptions) {
    super({
      query,
      json,
      dataKey: 'data.ActiveSession'
    })
  }
}
