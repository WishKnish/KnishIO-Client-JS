import Response from './Response';

export default class ResponsePolicy extends Response {
  /**
   * Class constructor
   *
   * @param {Query} query
   * @param {object} json
   */
  constructor ( {
    query,
    json
  } ) {
    super( {
      query,
      json
    } );
    this.dataKey = 'data.Policy';
    this.init();
  }

  /**
   *
   * @returns {null|object}
   */
  payload () {

    const policy = this.data();

    if ( !policy ) {
      return null;
    }

    if ( policy.callback ) {
      return JSON.parse( policy.callback );
    }

    return null;
  }
}
