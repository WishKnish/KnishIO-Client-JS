import Query from '../query/Query.js'
import Response from './Response.js'
import ResponseWalletList from './ResponseWalletList.js'

/**
 * Response for balance query
 */
export default class ResponseBalance extends Response {
  /**
   * Class constructor
   *
   * @param {Query} query
   * @param {object} json
   */
  constructor ({
    query,
    json
  }) {
    super({
      query,
      json,
      dataKey: 'data.Balance'
    })
  }

  /**
   * Returns a wallet with balance
   *
   * @return {null|Wallet}
   */
  payload () {
    let walletData = this.data()

    // Handle array response (e.g. from Rust validator which returns Vec<Wallet>)
    if (Array.isArray(walletData)) {
      walletData = walletData.length > 0 ? walletData[0] : null
    }

    if (!walletData || !walletData.bundleHash || !walletData.tokenSlug) {
      return null
    }

    return ResponseWalletList.toClientWallet({
      data: walletData
    })
  }
}
