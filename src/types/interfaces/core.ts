/**
 * Core interface definitions for Knish.IO SDK
 */

/**
 * Interface for version handling
 */
export interface VersionHandler {
  create(atom: any): {
    view(): any;
  };
  [key: string]: any;
}

/**
 * Interface for subscription options
 */
export interface SubscribeOptions {
  uri?: string;
  cellSlug?: string;
  variables?: Record<string, unknown>;
  onUpdate?: SubscriptionCallback;
  onError?: (error: Error) => void;
}

/**
 * Interface for subscription requests
 */
export interface SubscribeRequest {
  query: string;
  variables?: Record<string, unknown>;
  operationName?: string;
}

/**
 * Type alias for subscription callbacks
 */
export type SubscriptionCallback = (data: unknown) => void