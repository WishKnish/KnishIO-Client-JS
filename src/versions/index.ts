/**
 * Version registry for Knish.IO protocol versions
 * 
 * This module provides a centralized registry of supported protocol versions,
 * allowing the SDK to handle different atom structures and behaviors based on
 * the protocol version being used.
 * 
 * Currently supported versions:
 * - Version 4: Enhanced metadata support and improved batch handling
 * 
 * Each version handler implements the VersionHandler interface, providing:
 * - create(): Factory method for creating version-specific atom instances
 * - view(): Method for generating structured atom data for hashing
 */
